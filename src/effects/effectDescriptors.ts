export const EFFECT_DESCRIPTOR_MAP = new Map(
  EFFECT_DESCRIPTORS.map((d) => [d.id, d]),
);

export function getEffectDescriptor(
  effectId: string,
): EffectDescriptor | undefined {
  return EFFECT_DESCRIPTOR_MAP.get(effectId);
}

export function getEffectDefaultParams(effectId: string): Record<string, number> {
  const descriptor = EFFECT_DESCRIPTOR_MAP.get(effectId);
  if (!descriptor) return {};
  return Object.fromEntries(
    descriptor.parameters.map((p) => [p.templateId, p.defaultValue]),
  );
}
